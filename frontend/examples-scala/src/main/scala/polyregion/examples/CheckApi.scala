    Runtimes.load()

    val rs = Array(Runtimes.Relocatable(), Runtimes.OpenCL(), Runtimes.CUDA())


    rs.foreach { r =>
      println(r)
      val xs = r.devices()
      println(xs.map(x => s"\t$x\n${x.properties().map(x => s"\t\t$x").mkString("\n")}").mkString("\n"))

      xs.foreach { d0 =>

        println(s"D0 = $d0")
        val m = d0.malloc(10, Access.RW)
