        )
        q0.enqueueInvokeAsync(
          "a",
          "lambda",
          java.util.Arrays.asList(
            Arg.ptr(xs),
            Arg.of(42)
          ),
          Arg.of(),
          new Policy(new Dim3(1, 1, 1)),
          () => println("  K done! id=" + Thread.currentThread().getId)
        )
        q0.enqueueDeviceToHostAsync(
          xs,
          data,
          data.capacity(),
          () => {
